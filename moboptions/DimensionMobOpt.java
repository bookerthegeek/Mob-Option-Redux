package moboptions;

public class DimensionMobOpt {
	public int dimBaseDifficulty;
	public int dimMaxDifficulty;
	public int dimHealthCap;
	public int dimDamageCap;
		
	public DimensionMobOpt(int dimBaseDifficulty, int dimMaxDifficulty, int dimHealthCap, int dimDamageCap){
		this.dimBaseDifficulty = dimBaseDifficulty;
		this.dimMaxDifficulty = dimMaxDifficulty;
		this.dimHealthCap = dimHealthCap;
		this.dimDamageCap = dimDamageCap;
	}
}
